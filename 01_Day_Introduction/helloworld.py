# Checking data types
print(type('hello world')) # String
print(type(10)) # Int
print(type(3.14)) # Float
print(type(1 + 3j)) # Complex
print(type([1, 2, 3])) # List
print(type({'name': 'hello world'})) # Dictionary
print(type({1.1, 2.2, 3.3, 4.4})) # Set
print(type((1.1, 2.2, 3.3, 4.4))) # Tuple
print(type(3 == 3)) # Bool
print(type(3 >= 3)) # Bool

# Arithmetic operations
print(3 + 2) # Addition (+)
print(3 - 2) # Subtraction (-)
print(3 * 2) # Multiplication (*)
print(3 / 2) # Division (/)
print(3 ** 2) # Exponential (**)
print(3 % 2) # Modules (%)
print(3 // 2) # Floor division operator (//)